package com.javaprograms;

public class DoWhileandWhile {

	public static void main(String[] args) {
		// Do while and While loop examle
		
		int a=0;
		while(a<5)
		{
			System.out.println("concentrate first");
			a++;
		}

		
		//do While
		
		int c=0;
		do
		{
			System.out.println("hard work first");
		}
		while(++c<6);
	}

}
