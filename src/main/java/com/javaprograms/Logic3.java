package com.javaprograms;

public class Logic3 {

	public static void main(String[] args) {
		
		//display absolute difference between n and 21, expect display double the absolute difference if n is over 21.
		int num=2;
		if(num>=21)
		{
			System.out.println(2*(num-21));
		}
		else
		{
			System.out.println(21-num);
		}
				
	}

}
