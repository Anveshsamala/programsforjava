package com.javaprograms;

public class Switch1 {

	public static void main(String[] args) {
		int aaa=28;
		switch(aaa)
		{
			case 10: System.out.println("child age is 10");
			break;
			
			case 15: System.out.println("Teen age is 15");
			break;
			
			case 28: System.out.println("marriage age is 28");
			break;
			
			default : System.out.println("none of the above");
		}

	}

}
