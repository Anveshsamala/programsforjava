package com.javaprograms;

public class Logic8 {

	public static void main(String[] args) {
		// Multiply of two numbers without using Multiplication operator
		
		int num=12;
		int mul=7;
		int count=0;
		for(int i=0;i<mul;i++)
		{
			count=count+num;
		}
		System.out.println(count);

	}

}
